    // Generic wrapper sent by the client on every mutating SCL request.
    // RowVersion enables optimistic concurrency; LockToken proves the caller
    // holds the pessimistic element-level lock.
    // ?????????????????????????????????????????????????????????????????????????
    public class SclUpdateEnvelope<T>
    {
        [Required] public T Payload { get; set; } = default!;
        /// <summary>Base-64 RowVersion from the previous GET.</summary>
        public string? RowVersion { get; set; }
        public string? Comment    { get; set; }
    }

